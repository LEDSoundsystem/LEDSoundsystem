}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [_playlists.items count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Playlist Name" forIndexPath:indexPath];
    // Configure the cell...
    cell.textLabel.text=[self.playlists.items objectAtIndex:indexPath.row];
    
    return cell;
}


